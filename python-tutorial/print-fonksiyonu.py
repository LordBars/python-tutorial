# print fonksiyonu herhangi bir veri tipini konsola çıktı olarak gösterir.
print('Merhaba Dünya ben Mars')

# sep: argümanları ayırır
print("Merhaba", "Dünya", "Ben", "Mars", sep="\n") 

# end: printin sonundaki değeri değiştirir, normalde satır atlama (\n)
print("Merhaba", end= " ")
print("Dünya Ben Mars", end="!")