## Sayısal veri tipleri

a = 2 # int
b = 4.0 # float
c = 1j # Complex

print("A:" , a, "\n", type(a))
print("B:" , b, "\n", type(b))
print("C:" , c, "\n", type(c))

print("\n")

## String veri tipi

string = 'Merhaba'
string = "Merhaba"
string = """ Merhaba ben uzun string
            satırları atlayabilirim """
print("string: ", string, "\n", type(string))

print("\n")

## Boolean veri tipi

bool1 = True
bool1 = False
print("bool:", bool1, "\n", type(bool1))

print("\n")

## None tipi: Bir değişkenin değeri ve tipi olmadığını ifade eder.

none1 = None # Değer ve tip bilinmiyor
print("none1:", none1, "\n", type(none1)) 